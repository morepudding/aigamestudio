const EDEN_REPO = "aigamestudio";

/**
 * List all text files in the Eden Studio app repo (non-recursive wrapper
 * using the Git trees API for efficiency).
 */
export async function listEdenRepoFiles(): Promise<string[]> {
  const octokit = getOctokit();

  const SKIP = /^(node_modules|\.next|\.git|dist|out|public)\//;
  const INCLUDE = /\.(ts|tsx|js|jsx|json|md|css|html|txt|ya?ml)$/i;

  try {
    const { data: refData } = await octokit.git.getRef({
      owner: GITHUB_OWNER,
      repo: EDEN_REPO,
      ref: "heads/main",
    });

    const { data: commitData } = await octokit.git.getCommit({
      owner: GITHUB_OWNER,
      repo: EDEN_REPO,
      commit_sha: refData.object.sha,
    });

    const { data: treeData } = await octokit.git.getTree({
      owner: GITHUB_OWNER,
      repo: EDEN_REPO,
      tree_sha: commitData.tree.sha,
      recursive: "true",
    });

    return (treeData.tree ?? [])
      .filter((item) => item.type === "blob" && !!item.path)
      .map((item) => item.path as string)
      .filter((p) => !SKIP.test(p) && INCLUDE.test(p));
  } catch {
    return [];
  }
}

/**
 * Read a file from the Eden Studio app repo.
 * Returns null if not found.
 */
export async function readEdenFile(path: string): Promise<string | null> {
  return getFileContent(EDEN_REPO, path);
}

/**
 * Build a focused codebase summary for Eve.
 * Returns file tree + content of up to 6 key files (prioritised by relevance).
 */
export async function buildEdenCodebaseContext(
  relevantPaths?: string[]
): Promise<string> {
  const allFiles = await listEdenRepoFiles();
  const fileTree = allFiles.map((p) => `  ${p}`).join("\n");

  const pathsToRead = relevantPaths?.length
    ? relevantPaths.slice(0, 6)
    : allFiles
        .filter(
          (p) =>
            p.startsWith("app/") ||
            p.startsWith("lib/") ||
            p === "CLAUDE.md"
        )
        .slice(0, 6);

  const fileContents = await Promise.all(
    pathsToRead.map(async (p) => {
      const content = await readEdenFile(p);
      if (!content) return null;
      const preview = content.split("\n").slice(0, 80).join("\n");
      return `\n### ${p}\n\`\`\`\n${preview}${content.split("\n").length > 80 ? "\n// ...truncated" : ""}\n\`\`\``;
    })
  );

  const snippets = fileContents.filter(Boolean).join("\n");

  return `## Repo Eden Studio (morepudding/aigamestudio)\n\n### Fichiers disponibles\n${fileTree}\n${snippets}`;
}

export interface RepoTextFileSnapshot {
  path: string;
  content: string;
}

const SNAPSHOT_FILE_REGEX = /\.(ts|tsx|js|jsx|json|md|css|html|txt|ya?ml)$/i;
const SNAPSHOT_SKIP_REGEX = /(^|\/)(node_modules|dist|build|coverage|\.git|public)(\/|$)/;

function getOctokit(): Octokit {
  const token = process.env.GITHUB_TOKEN;
  if (!token) {
    throw new Error("GITHUB_TOKEN is not defined");
  }
  return new Octokit({ auth: token });
}

