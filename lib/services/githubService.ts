/**
 * Push (create or update) a file in the repository.
 */
export async function pushFile(
  repoName: string,
  path: string,
  content: string,
  commitMessage: string
): Promise<void> {
  const octokit = getOctokit();

  // GitHub rejects paths starting with "./" or ending with a slash
  path = path.replace(/^\.\//, "").replace(/\/+$/, "");
  if (!path) throw new Error("pushFile: path cannot be empty");

  // Check if file already exists to get its SHA (required for updates)
  let existingSha: string | undefined;
  try {
    const { data } = await octokit.repos.getContent({
      owner: GITHUB_OWNER,
      repo: repoName,
      path,
    });
    if (!Array.isArray(data) && data.type === "file") {
      existingSha = data.sha;
    }
  } catch {
    // File does not exist — will be created
  }

  await octokit.repos.createOrUpdateFileContents({
    owner: GITHUB_OWNER,
    repo: repoName,
    path,
    message: commitMessage,
    content: Buffer.from(content).toString("base64"),
    sha: existingSha,
  });
}

/**
 * Read the decoded content of a file from the repository.
 * Returns null if the file does not exist.
 */
export async function getFileContent(
  repoName: string,
  path: string
): Promise<string | null> {
  const octokit = getOctokit();

  try {
    const { data } = await octokit.repos.getContent({
      owner: GITHUB_OWNER,
      repo: repoName,
      path,
    });

    if (Array.isArray(data) || data.type !== "file") {
      return null;
    }

    if ("content" in data && data.content) {
      return Buffer.from(data.content, "base64").toString("utf-8");
    }

    return null;
  } catch {
    return null;
  }
}

/**
 * List file paths inside a directory (non-recursive).
 * Returns an empty array if the path does not exist.
 */
export async function listFiles(
  repoName: string,
  path: string = ""
): Promise<string[]> {
  const octokit = getOctokit();

  try {
    const { data } = await octokit.repos.getContent({
      owner: GITHUB_OWNER,
      repo: repoName,
      path,
    });

    if (!Array.isArray(data)) {
      return [];
    }

    return data.map((item) => item.path);
  } catch {
    return [];
  }
}

