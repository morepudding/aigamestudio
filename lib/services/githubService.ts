/**
 * Create a new private GitHub repository under the owner account.
 * Idempotent: if the repo already exists, returns its info instead of throwing.
 */
export async function createRepo(
  name: string,
  description: string
): Promise<{ url: string; fullName: string }> {
  const octokit = getOctokit();

  try {
    const { data } = await octokit.repos.createForAuthenticatedUser({
      name,
      description,
      private: true,
      auto_init: true,
    });
    return { url: data.html_url, fullName: data.full_name };
  } catch (err: unknown) {
    // 422 = repo already exists — return existing repo info
    if (
      err &&
      typeof err === "object" &&
      "status" in err &&
      (err as { status: number }).status === 422
    ) {
      const { data } = await octokit.repos.get({ owner: GITHUB_OWNER, repo: name });
      return { url: data.html_url, fullName: data.full_name };
    }
    throw err;
  }
}

/**
 * Push an initial directory structure to a freshly created repo.
 * Uses a multi-file commit via the Git Trees API (single commit, no repeated calls).
 */
export async function initRepoStructure(
  repoName: string,
  projectTitle: string
): Promise<void> {
  const octokit = getOctokit();

  // Files to create in the initial commit
  const initialFiles: { path: string; content: string }[] = [
    {
      path: "README.md",
      content: `# ${projectTitle}\n\nGenerated by Eden Studio.\n`,
    },
    {
      path: "docs/gdd.md",
      content: `# Game Design Document — ${projectTitle}\n\n> À remplir par le Game Designer.\n`,
    },
    {
      path: "docs/tech-spec.md",
      content: `# Spécification Technique — ${projectTitle}\n\n> À remplir par le Lead Dev.\n`,
    },
    {
      path: "docs/data-arch.md",
      content: `# Architecture Data & État — ${projectTitle}\n\n> À remplir.\n`,
    },
    {
      path: "docs/asset-list.md",
      content: `# Asset List — ${projectTitle}\n\n> À remplir par le Lead Artist.\n`,
    },
    {
      path: "docs/backlog.md",
      content: `# Backlog de Développement — ${projectTitle}\n\n> À générer par le Producer.\n`,
    },
    { path: "src/.gitkeep", content: "" },
    { path: "assets/.gitkeep", content: "" },
  ];

  // Get the current HEAD of main (created by auto_init)
  const { data: refData } = await octokit.git.getRef({
    owner: GITHUB_OWNER,
    repo: repoName,
    ref: "heads/main",
  });
  const baseCommitSha = refData.object.sha;

  const { data: baseCommit } = await octokit.git.getCommit({
    owner: GITHUB_OWNER,
    repo: repoName,
    commit_sha: baseCommitSha,
  });
  const baseTreeSha = baseCommit.tree.sha;

  // Create blobs sequentially to avoid git-lock conflicts
  const blobs: { data: { sha: string } }[] = [];
  for (const file of initialFiles) {
    const blob = await octokit.git.createBlob({
      owner: GITHUB_OWNER,
      repo: repoName,
      content: Buffer.from(file.content).toString("base64"),
      encoding: "base64",
    });
    blobs.push(blob);
  }

  // Create tree on top of existing base tree
  const { data: treeData } = await octokit.git.createTree({
    owner: GITHUB_OWNER,
    repo: repoName,
    base_tree: baseTreeSha,
    tree: initialFiles.map((file, idx) => ({
      path: file.path,
      mode: "100644" as const,
      type: "blob" as const,
      sha: blobs[idx].data.sha,
    })),
  });

  // Create commit with parent
  const { data: commitData } = await octokit.git.createCommit({
    owner: GITHUB_OWNER,
    repo: repoName,
    message: "chore: init Eden Studio project structure",
    tree: treeData.sha,
    parents: [baseCommitSha],
  });

  // Update main branch to point to new commit
  await octokit.git.updateRef({
    owner: GITHUB_OWNER,
    repo: repoName,
    ref: "heads/main",
    sha: commitData.sha,
  });
}

/**
 * Ensure the base concept docs exist in the repository.
 * This repairs repos that were created before the full bootstrap completed.
 */
export async function ensureProjectDocs(
  repoName: string,
  projectTitle: string
): Promise<void> {
  const expectedDocs: { path: string; content: string }[] = [
    {
      path: "docs/gdd.md",
      content: `# Game Design Document — ${projectTitle}\n\n> À remplir par le Game Designer.\n`,
    },
    {
      path: "docs/tech-spec.md",
      content: `# Spécification Technique — ${projectTitle}\n\n> À remplir par le Lead Dev.\n`,
    },
    {
      path: "docs/data-arch.md",
      content: `# Architecture Data & État — ${projectTitle}\n\n> À remplir.\n`,
    },
    {
      path: "docs/asset-list.md",
      content: `# Asset List — ${projectTitle}\n\n> À remplir par le Lead Artist.\n`,
    },
    {
      path: "docs/backlog.md",
      content: `# Backlog de Développement — ${projectTitle}\n\n> À générer par le Producer.\n`,
    },
  ];

  const existingDocs = new Set(await listFiles(repoName, "docs"));

  for (const doc of expectedDocs) {
    if (existingDocs.has(doc.path)) continue;
    await pushFile(
      repoName,
      doc.path,
      doc.content,
      `[eden] bootstrap: add missing ${doc.path.split("/").pop()}`
    );
  }
}

