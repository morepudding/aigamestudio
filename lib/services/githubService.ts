/**
 * Active GitHub Pages sur la branche main du repo.
 * Idempotent : si Pages est déjà actif, retourne l'URL existante.
 * Retourne l'URL publique du mini-jeu.
 */
export async function enableGithubPages(repoName: string): Promise<string> {
  const octokit = getOctokit();

  // Vérifier si Pages est déjà actif
  try {
    const { data } = await octokit.repos.getPages({
      owner: GITHUB_OWNER,
      repo: repoName,
    });
    return data.html_url ?? "";
  } catch {
    // Pages non actif — on l'active
  }

  await octokit.repos.createPagesSite({
    owner: GITHUB_OWNER,
    repo: repoName,
    source: { branch: "main", path: "/" },
  });

  // L'URL est prévisible même avant que le build soit prêt
  return `https://${GITHUB_OWNER}.github.io/${repoName}/`;
}

/**
 * Attend que GitHub Pages ait terminé son déploiement.
 * Poll toutes les 5s jusqu'à ce que l'URL réponde avec un 2xx ou timeout (3 min).
 * Retourne l'URL finale.
 */
export async function waitForPagesDeployment(
  pagesUrl: string,
  timeoutMs = 180_000
): Promise<{ ready: boolean; url: string }> {
  const deadline = Date.now() + timeoutMs;
  const interval = 5_000;

  while (Date.now() < deadline) {
    try {
      const res = await fetch(pagesUrl, { method: "HEAD" });
      if (res.ok) return { ready: true, url: pagesUrl };
    } catch {
      // Pas encore prêt
    }
    await new Promise((resolve) => setTimeout(resolve, interval));
  }

  return { ready: false, url: pagesUrl };
}

function scoreSnapshotPath(path: string): number {
  if (path === "README.md") return 100;
  if (path === "docs/data-arch.md") return 95;
  if (path === "docs/asset-list.md") return 90;
  if (path.startsWith("src/assets/")) return 80;
  if (path.startsWith("src/core/")) return 75;
  if (path.startsWith("src/state/")) return 70;
  if (path.startsWith("src/systems/")) return 65;
  if (path.startsWith("src/components/")) return 60;
  if (path.startsWith("docs/")) return 30;
  return 10;
}

/**
 * Read a focused recursive snapshot of the repository text files.
 * Prioritises source files and supporting docs that are most useful for LLM analysis.
 */
export async function getRepositorySnapshot(
  repoName: string,
  options?: { maxFiles?: number; maxFileChars?: number }
): Promise<RepoTextFileSnapshot[]> {
  const octokit = getOctokit();
  const maxFiles = options?.maxFiles ?? 12;
  const maxFileChars = options?.maxFileChars ?? 5000;

  const { data: repo } = await octokit.repos.get({
    owner: GITHUB_OWNER,
    repo: repoName,
  });

  const { data: refData } = await octokit.git.getRef({
    owner: GITHUB_OWNER,
    repo: repoName,
    ref: `heads/${repo.default_branch}`,
  });

  const { data: commitData } = await octokit.git.getCommit({
    owner: GITHUB_OWNER,
    repo: repoName,
    commit_sha: refData.object.sha,
  });

  const { data: treeData } = await octokit.git.getTree({
    owner: GITHUB_OWNER,
    repo: repoName,
    tree_sha: commitData.tree.sha,
    recursive: "true",
  });

  const candidatePaths = (treeData.tree ?? [])
    .filter((item) => item.type === "blob" && !!item.path)
    .map((item) => item.path as string)
    .filter((path) => SNAPSHOT_FILE_REGEX.test(path))
    .filter((path) => !SNAPSHOT_SKIP_REGEX.test(path))
    .sort((left, right) => {
      const scoreDiff = scoreSnapshotPath(right) - scoreSnapshotPath(left);
      return scoreDiff !== 0 ? scoreDiff : left.localeCompare(right);
    })
    .slice(0, maxFiles);

  const files = await Promise.all(
    candidatePaths.map(async (path) => {
      const content = await getFileContent(repoName, path);
      if (!content) return null;
      return {
        path,
        content: content.length > maxFileChars ? `${content.slice(0, maxFileChars)}\n\n[...truncated]` : content,
      } satisfies RepoTextFileSnapshot;
    })
  );

  return files.filter((file): file is RepoTextFileSnapshot => file !== null);
}
